// Define a function called multiplyFive which accepts a number and 
// returns that number multiplied by 5.


 var multiplyFive = function(myNumber)
 {
    return (myNumber * 5);
 }
console.log(multiplyFive(3));
console.log(multiplyFive(4));
console.log(multiplyFive(5));
